import { chromium } from '@playwright/test'
const b = await chromium.launch()
const p = await b.newPage()
const msgs = []
p.on('console', (m) => msgs.push(`[${m.type()}] ${m.text().slice(0, 120)}`))
p.on('pageerror', (e) => msgs.push(`[pageerror] ${e.message.slice(0, 120)}`))
await p.goto('http://localhost:3000/admin/login', { waitUntil: 'networkidle' })
await p.fill('#field-email', 'dev@payloadcms.com')
await p.fill('#field-password', 'test')
await p.click('button[type="submit"]')
await p.waitForURL('**/admin', { timeout: 20000 }).catch(() => {})
await p.goto('http://localhost:3000/admin/collections/inquiries', { waitUntil: 'networkidle' })
await p.waitForTimeout(2500)
const hydration = msgs.filter((m) => /hydrat|did not match|disabled/i.test(m))
console.log('TOTAL console msgs:', msgs.length)
console.log('HYDRATION-RELATED:', hydration.length)
hydration.slice(0, 4).forEach((m) => console.log('  ', m))
console.log('ERRORS:', msgs.filter((m) => m.startsWith('[error]') || m.startsWith('[pageerror]')).length)
await b.close()
